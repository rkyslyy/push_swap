#include "libft.h"

int			ft_strnequ(char const *s1, char const *s2, size_t n)
{
	size_t	i;

	if (s1 && s2)
	{
		if (s1[0] == '\0' || s2[0] == '\0')
			return (1);
		i = 0;
		while (s1[i] == s2[i])
		{
			if ((s1[i] == '\0' && s2[i] == '\0') || i == n - 1)
				return (1);
			i++;
		}
	}
	return (0);
}
