#include "../../includes/prlib.h"

int		ft_smoker_atoi(const char *nptr)
{
	int m;
	int n;

	m = 0;
	n = 0;
	while (nptr[n] >= '0' && nptr[n] <= '9')
	{
		m = m * 10 + (nptr[n] - '0');
		n++;
	}
	return (m);
}
