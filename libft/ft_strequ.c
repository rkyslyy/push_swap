#include "libft.h"

int	ft_strequ(char const *s1, char const *s2)
{
	size_t	n;

	if (s1 && s2)
	{
		if (s1[0] == '\0' || s2[0] == '\0')
			return (1);
		n = 0;
		while (s1[n] == s2[n])
		{
			if (s1[n] == '\0' && s2[n] == '\0')
				return (1);
			n++;
		}
	}
	return (0);
}
