#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	n;

	n = 0;
	while (*s != '\0')
	{
		s++;
		n++;
	}
	while (n >= 0)
	{
		if (*s == c)
			return ((char*)s);
		s--;
		n--;
	}
	return (NULL);
}
